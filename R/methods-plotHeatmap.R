#' Plot Heatmap
#'
#' Construct a simple heatmap. By default, row- and column-wise hierarchical
#' clustering is performed using the Ward method, but this behavior can be
#' overrided by setting `cluster_rows` or `cluster_cols` to `FALSE`.
#'
#' @rdname plotHeatmap
#' @name plotHeatmap
#'
#' @param annotationCol *Optional*. [data.frame] that specifies the annotations
#'   shown on the right side of the heatmap. Each row of this [data.frame]
#'   defines the features of the heatmap columns.
#' @param scale Character indicating if the values should be centered and scaled
#'   in either the row direction or the column direction, or none. Corresponding
#'   values are "row", "column" and "none".
#' @param color Colors to use for plot. Defaults to the [viridis::viridis()]
#'   palette.
#' @param legendColor Colors to use for legend labels. Defaults to the
#'   [viridis::viridis()] palette.
#' @param title *Optional*. Plot title.
#' @param ... Passthrough arguments to [pheatmap::pheatmap()].
#'
#' @seealso [pheatmap::pheatmap()].
#'
#' @return [pheatmap::pheatmap()] return [list], containing `gtable`.
#'
#' @examples
#' mat <- as.matrix(mtcars)
#' plotHeatmap(mat)
NULL



# Constructors =================================================================
#' @importFrom dplyr mutate_all
#' @importFrom grDevices colorRampPalette
#' @importFrom pheatmap pheatmap
#' @importFrom RColorBrewer brewer.pal
#' @importFrom stats setNames
#' @importFrom tibble column_to_rownames rownames_to_column
#' @importFrom viridis viridis
.plotHeatmap <- function(
    object,
    annotationCol = NULL,
    scale = "row",
    color = viridis::viridis(256),
    legendColor = viridis::viridis,
    title = NULL,
    ...) {
    counts <- as.matrix(object)

    # Drop rows that are all zero, when row scaling is applied
    if (scale == "row") {
        counts <- counts[rowSums(counts) > 0, , drop = FALSE]
    }

    if (nrow(counts) < 2) {
        stop("Need at least 2 rows to plot heatmap", call. = FALSE)
    }
    if (ncol(counts) < 2) {
        stop("Need at least 2 columns to plot heatmap", call. = FALSE)
    }

    # Convert Ensembl gene identifiers to symbol names, if necessary
    if (nrow(counts) <= 100) {
        showRownames <- TRUE
    } else {
        showRownames <- FALSE
    }

    # Prepare the annotation columns, if necessary
    if (!is.null(annotationCol)) {
        annotationCol <- annotationCol %>%
            as.data.frame() %>%
            .[rownames(counts), , drop = FALSE] %>%
            rownames_to_column() %>%
            mutate_all(factor) %>%
            column_to_rownames()
    }

    # Define colors for each annotation column, if desired
    if (is.data.frame(annotationCol) & is.function(legendColor)) {
        annotationColors <- lapply(
            seq_along(colnames(annotationCol)), function(a) {
                col <- annotationCol[[a]] %>%
                    levels()
                colors <- annotationCol[[a]] %>%
                    levels() %>%
                    length() %>%
                    legendColor
                names(colors) <- col
                colors
            }) %>%
            setNames(colnames(annotationCol))
    } else {
        annotationColors <- NULL
    }

    # pheatmap will error if `NULL` title is passed as `main`
    if (is.null(title)) {
        title <- ""
    }

    # If `color = NULL`, use the pheatmap default
    if (!is.character(color)) {
        color <- colorRampPalette(rev(brewer.pal(n = 7, name = "RdYlBu")))(100)
    }

    pheatmap(
        mat = counts,
        annotation_col = annotationCol,
        annotation_colors = annotationColors,
        border_color = NA,
        color = color,
        main = title,
        scale = scale,
        show_rownames = showRownames,
        ...)
}



# Methods ======================================================================
#' @rdname plotHeatmap
#' @importFrom viridis viridis
#' @export
setMethod(
    "plotHeatmap",
    signature("matrix"),
    .plotHeatmap)
